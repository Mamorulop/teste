#Operaçoes Aritméticas
# + = ADIÇÃO
# - = SUBTRAÇÃO
# * = MULTIPLICAÇÃO
# / = DIVISÃO
# ** = POTÊNCIA
#  / / = DIVISÃO INTEIRA
#  %  = RESTO DA DIVISÃO
n1 = int(input("Digite um número:"))
n2 = int(input("Digite um número:"))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print(f"A soma é {s}, o produto é {m} e a divisão é {d}", end="")
print(f"Divisão inteira é {di} e a potência é {e}")
    
